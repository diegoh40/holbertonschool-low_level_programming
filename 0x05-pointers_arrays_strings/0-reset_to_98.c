/**
 * reset_to_98 - takes a pointer to an int as parameter and updates 
 *
 * Return: Always 0.
 */
void reset_to_98(int *n);
{
    int n;

    n = 402;
    
    printf("n=%d\n", n);

    reset_to_98(&n);
    *n = 98;
    
    printf("*n=%d\n", *n);

    return (0);
    
}
